# 优先级从高到低, 从左到右求值
# **     指数          2 ** 3  8
# %      取模/取余数    22 % 8  6
# //     整除/商数取整  22 // 8  2
# /      除法          22 / 8   2.75
# *      乘法          3 * 5    15
# -      减法          5 - 2    3
# +      加法          2 + 2    4
print(2 ** 3)
print(22 % 8)
print(22 // 8)
print(22 / 8)
print(3 * 5)
print(5 - 2)
print(2 + 2)