# “比较操作符”比较两个值，求值为一个布尔值
# == 等于
# != 不等于
# <  小于
# >  大于
# <= 小于等于
# >= 大于等于
print(42 == 42)
print(43 == 42)
print(3 != 2)
print(2 != 2)

# == 和 != 操作符可以用于所有数据类型的值
print('hello' == 'hello')
print('hell' != 'hello')
print(42 == 42.0)
print(42 == '42')
# <、>、<=和>=操作符仅用于整型和浮点型值
print(43 < 100)
print(43 > 100)
eggCount = 1
print(eggCount == 1)
print(eggCount < 10)